// Banco de preguntas de opción múltiple por categoría (FR -> ES).
// Las opciones incorrectas se generan en el componente mezclando otras
// palabras de la misma categoría, así que aquí solo guardamos la base.
export function buildQuizBank(categoryId) {
  const words = vocabWords[categoryId]
  return words.map((word, index) => {
    const distractors = words
      .filter((_, i) => i !== index)
      .map((w) => w.es)
      .sort(() => Math.random() - 0.5)
      .slice(0, 3)
    const options = [word.es, ...distractors].sort(() => Math.random() - 0.5)
    return {
      question: word.fr,
      options,
      correctAnswer: word.es,
    }
  })
}
